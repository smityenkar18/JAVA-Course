public class Relational_Operators {
    public static void main(String[] args) {
        System.out.println("Relational Operators <,>,=,==,!=,<=,>=");
        
        int x =6;
        int y=5;
        boolean result = x <y;
        System.out.println(result);
        
        result = x >y;
        System.out.println(result);
        
        result = x>=y;
        System.out.println(result);
        
        result= x!=y;
        System.out.println(result);
        
        result= x==y;
        System.out.println(result);
        
        
    }
}
