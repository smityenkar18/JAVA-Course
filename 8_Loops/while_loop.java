public class while_loop {
    public static void main(String[] args) {
        System.out.println("WHILE LOOPS");
        
        int i =1;
        
        while(i<=5)
        {
        System.out.println("Hi"+i);
        i++;
        }
        System.out.println("Bye "+ i);
    }
} 
